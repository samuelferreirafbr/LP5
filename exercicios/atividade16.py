combustivel = input("Digite o tipo de combustível (gasolina, etanol, diesel): ")

if combustivel == "gasolina":
    print("O preço da gasolina é R$ 5,00 por litro.")
elif combustivel == "etanol":
    print("O preço do etanol é R$ 3,50 por litro.")
elif combustivel == "diesel":
    print("O preço do diesel é R$ 4,00 por litro.")