cor = input('Digite uma cor (vermelho, azul, verde): ')

if cor == "vermelho":
    print("Olá! Você escolheu a cor vermelha")
elif cor == "verde":
    print("Olá! Você escolheu a cor verde")
elif cor == "azul":
    print("Olá! Você escolheu a cor azul")
else:
    print("Mensagem inválida! Digite apenas vermelho, verde ou azul.")
