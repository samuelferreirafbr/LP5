entrada = input('Digite seu estado civil (solteiro, casado, divorciado, viuvo): ')
if entrada == 'solteiro':
    print("Você é solteiro.")
elif entrada == 'casado':
    print("Você é casado.")
elif entrada == 'divorciado':
    print("Você é divorciado.")
elif entrada == 'viuvo':
    print("Você é viuvo.")
else:
    print("Estado civil inválido! Digite solteiro, casado, divorciado ou viuvo.")